package lessons.lesson10;

public class Car {

    private String number;
    private int year;

    public Car() {

    }

    public Car(String n) {
        number = n;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }


}
